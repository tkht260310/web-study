// 数値と計算
// 数値は文字列と違い"◯",'◯'で囲まない
console.log(3);
console.log(5 + 2);
console.log(3 - 2);

// 文字列と数値
console.log(5 + 2);
console.log("5 + 2");
// 上が数値、下が文字列　明確に違うもの

//　演習問題
// 5と3を足した値を出力してください
console.log(5 + 3);
// 20から8を引いた値を出力してください
console.log(20 - 8);
// "4 + 5" を文字列として出力してください
console.log("4 + 5");

// 掛け算・割り算
console.log(3 * 7);
console.log(6 / 3);
// 掛け算は*アスタリスク、割り算は/スラッシュを使うよ
// 余りの計算は%パーセントを使うよ
console.log(9 % 2);
