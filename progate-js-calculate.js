// 数値と計算
// 数値は文字列と違い"◯",'◯'で囲まない
console.log(3);
console.log(5 + 2);
console.log(3 - 2);

// 文字列と数値
console.log(5 + 2);
console.log("5 + 2");
// 上が数値、下が文字列　明確に違うもの

